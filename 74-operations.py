def finalValueAfterOperations(operations):
        """
        :type operations: List[str]
        :rtype: int
        """
        sum=0

        for e in operations:
                if e.count("+")!=0:
                        sum+=1
                else:
                        sum-=1

        
        return sum

ans=finalValueAfterOperations(["X++","++X","--X","X--"])
print(ans)

